#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int len;
	int	i;

	len = 0;
	i = 0;
	while (s[len] != '\0')
		len++;
	len++;
	while (i < len)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}
